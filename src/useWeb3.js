// 使用 Web3.js 的例子
const Web3 = require('web3');
const web3 = new Web3('https://rpcpc1-qa.agung.peaq.network');

// 合约地址
const contractAddress = "0x0814f962baecd118de01209889f5c2c9b4aacaaa"; // 替换为实际的合约地址

// 私钥
const privateKey = "...";

// 使用私钥创建以太坊账户
const account = web3.eth.accounts.privateKeyToAccount(privateKey);

const myContract = new web3.eth.Contract(contractAbi, contractAddress);

console.log(account.address);

// 使用以太坊账户创建交易对象
const transactionObject = {
    from: account.address,
    to: contractAddress,
    gas: 500000, // gas 限制
    gasPrice: 4100, // gas 价格
    data: myContract.methods.mint(51000).encodeABI(),
};

// 使用以太坊账户签署交易
web3.eth.accounts.signTransaction(transactionObject, account.privateKey)
    .then((signedTransaction) => {
        // 发送签名交易到区块链
        console.log(signedTransaction)
        web3.eth.sendSignedTransaction(signedTransaction.rawTransaction)
            .on('transactionHash', (hash) => {
                console.log('Transaction Hash:', hash);
            })
            .on('receipt', (receipt) => {
                console.log('Transaction Receipt:', receipt);
            })
            .on('error', (error) => {
                console.error('Transaction Error:', error);
            });
    })
    .catch((error) => {
        console.error('Signing Error:', error);
    });